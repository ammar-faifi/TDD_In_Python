"""
This module provides an example of calculator moethods to be used
later with `uittest` module for testing.
"""

def add(num1, num2):
    """Adds `num1` and `num2`"""

    # raise Exception("Erorr in `add`")
    # return None
    return num1 + num2

def sub(num1, num2):
    """Subtracts `num2` form `num1`"""

    return num1 - num2

def mul(num1, num2):
    """Muliplies `num1` and `num2`"""

    return num1 * num2

def div(num1, num2):
    """Divides `num1` on `num2`"""

    return num1 / num2
